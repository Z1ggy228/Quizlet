let bag = []

function refill() {
  bag = [...PRAISE]
  for (let i = bag.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[bag[i], bag[j]] = [bag[j], bag[i]]
  }
}

/** Следующая похвала — без повторов, пока не кончится весь список. */
export function nextPraise() {
  if (!bag.length) refill()
  return bag.pop()
}
